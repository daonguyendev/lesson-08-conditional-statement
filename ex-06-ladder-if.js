let score = 75;

if(score >=0 && score <= 100) {
    if (score >= 90.0) {
        console.log("A");
    } else if (score >= 80.0) {
        console.log("B");
    } else if (score >= 70.0) {
        console.log("C");
    } else if (score >= 60.0) {
        console.log("D");
    } else {
        console.log("F");
    }
} else {
    console.log("Nhap lai diem so trong khoang tu 0 den 100");
}


